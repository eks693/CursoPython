def adicionar(a,b):
    return a + b

def subtrair(a,b):
    return a - b

def multiplicar(a,b):
    return a * b

def dividir(a,b):
    if b == 0:
        return "Divisão por zero não é permitida"
    else:
        return a / b

def potencia(a,b):
    return a ** b   

def porcentagem(a,b):
    return (a * b) / 100

def raiz(a):
    return a ** (1/2)